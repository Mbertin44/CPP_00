#include "../includes/Fixed.hpp"

/*
	L'interet de convertir un int et un float en valeur fixe et que je vais pouvoir comparer ces deux valeurs avec plus de précision.
	Comparer un int et un float en faisant ca ne sera pas assez précis car si mon int et mon float sont égal à 8 en realite le float sera égal (exemple arbitraire) à 8.000000001.

	Pour résumer c'est surtout une conversion binaire stock dans int.
*/

int main( void )
{
	Fixed a;
	Fixed const b( 10 );
	Fixed const c( 42.42f );
	Fixed const d( b );

	a = Fixed( 1234.4321f );

	std::cout << "a is " << a << std::endl;
	std::cout << "b is " << b << std::endl;
	std::cout << "c is " << c << std::endl;
	std::cout << "d is " << d << std::endl;

	std::cout << "a is " << a.toInt() << " as integer" << std::endl;
	std::cout << "b is " << b.toInt() << " as integer" << std::endl;
	std::cout << "c is " << c.toInt() << " as integer" << std::endl;
	std::cout << "d is " << d.toInt() << " as integer" << std::endl;
	return 0;
}